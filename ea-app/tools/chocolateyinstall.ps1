$checksum = '48a74ea132f4451e1cbd423a0a0d69d6a60ac975e5bdf05ed70e3b20e04bacf4'

$packageArgs = @{
  packageName    = $env:ChocolateyPackageName
  unzipLocation  = $toolsDir
  fileType       = 'EXE'
